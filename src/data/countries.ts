const toFlagEmoji = (iso2: string) =>
  iso2
    .toUpperCase()
    .replace(/[^A-Z]/g, "")
    .split("")
    .map((c) => 0x1f1e6 - 65 + c.charCodeAt(0))
    .map((cp) => String.fromCodePoint(cp))
    .join("");

